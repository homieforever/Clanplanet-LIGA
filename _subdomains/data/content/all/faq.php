<h2>Fragen / Antworten im ausgeloggten zustand</h2>
<br /><br />

Hier findest du einige häufig gestellte Fragen und die dazugehörigen Antworten zur Clanplanet LIGA.

<br /><br />

<strong>Allgemeine Fragen</strong>
<br /><br />
<div style="padding:8px;">
    
    1. Was ist das Projekt Clanplanet LIGA?<br />
    <div style="padding-left:20px;">
        <small>
            Die Clanplanet LIGA ist ein Projekt der Clanplanet Community.
            Sie soll allen Clans, Gilden und Alliazen auf einer großen Plattform vereinen und diesen viele möglichkeiten für Clanwars bzw Turniere bieten.
        </small>
    </div>
    
    <br /><br />
    
    2. Okay, aber warum "Clanplanet" LIGA?<br />
    <div style="padding-left:20px;">
        <small>
            Die Clanplanet LIGA ist zwar ein Clanplanet unabhängiges Projekt ist aber trotzdem an den Clanplanet gebunden.
            Die Clanplanet LIGA gibt nur Clanplanet Mitglieder zur Registrierung frei und lässt auch nur Clanplanet Clans an unserer Community teilnehmen.
            Wir sind schlicht und einfach der Meinung das der Clanplanet solch eine Community gebrauchen kann.
        </small>
    </div>
    
    <br /><br />
    
    3. Was macht die Clanplanet LIGA gegenüber anderer Clanplanet Liegen anders?<br />
    <div style="padding-left:20px;">
        <small>
            Wir sind die einzige automatiesierte LIGA auf dem Clanplanet welche zugleich mit den Daten des Clanplanets arbeitet.
            Als Beispiel erkennen wir es wenn du deinen Benutzernamen auf dem Clanplanet änderst und werden diese Änderung ebenfalls auch auf der Clanplanet LIGA einleiten.
        </small>
    </div>
    
    <br /><br />
    
    4. Ist die Clanplanet LIGA kostenlos?<br />
    <div style="padding-left:20px;">
        <small>
            Grundsätzlich ist die Nutzung der Clanplanet LIGA komplett kostenlos.
            Wir bieten allerdings die möglichkeit durch Gems welche eine Art Währung auf der Clanplanet LIGA darstellen weitere Funktionen freizuschalten.
            Gems erhällt man durch diverse Aktivitäten auf der Clanplanet LIGA es ist also nicht nötig für Gems zu zahlen. Nur Nutzern denen das Gems verdienen zu lange dauert,
            wird die Möglichkeit geboten eine Spende zwischen 5€ und 50€ an uns zu leiten. Trotzdessen das wir bei Spenden nicht verpflichtet sind eine Gegenleistung zu erbringen, 
            übergeben wir den Nutzern für ihre spenden Gems oder/und Erfahrungspunkte.
        </small>
    </div>
    
    <br /><br />
    
    5. Zur Anmeldung muss ich keinerlei Daten angeben, wo bekomme ich dann Nachrichten der LIGA?<br />
    <div style="padding-left:20px;">
        <small>
            Die Clanplanet LIGA hat sich für diese Art der Anmeldung entschieden weil wir es Nutzern so einfach wie möglich gestalten möchten.
            Genau deshalb erhällst du Nachrichten der Clanplanet LIGA per PM auf dem Clanplanet.
            Dabei bitten wir um Vorsicht! Nachrichten von uns erhällst du auschließlich von dem Clanplanet Account "Clanplanet LIGA", über einen anderen Namen werden wir dich nicht kontaktieren.
        </small>
    </div>
    
    <br /><br />
    
    6. Ein anderer Account gibt sich als Clanplanet LIGA aus.<br />
    <div style="padding-left:20px;">
        <small>
            Solltest du Nachrichten auf dem Clanplanet bekommen, indem geschrieben wird das es die Clanplanet LIGA ist.
            Aber der Account nicht "Clanplanet LIGA" heisst, so kontaktiere bitte den Kundenservice der Clanplanet LIGA.
            Wir werden dir genauere Auskunft geben ob diese Nachricht wirklich nicht aus unserem System stammt und sofern diese Nachricht nicht von uns war, dagegen vorgehen.
        </small>
    </div>
    
    <br /><br />
    
    7. Wie lange gibt es die Clanplanet LIGA nun schon?<br />
    <div style="padding-left:20px;">
        <small>
            Die Clanplanet LIGA in dieser Form existiert seit Anfang 2013.
            Die erste Gründung erfolgte allerdings schon im Jahr 2009 und der erste Ansatz zur automatiesierung bereits im Jahr 2010.
        </small>
    </div>
    
</div>

<br /><br />

<strong>Fragen zur Registrierung</strong>
<br /><br />
<div style="padding:8px;">
    
    1. Wie melde ich mich an?<br />
    <div style="padding-left:20px;">
        <small>
            Mit der Anmeldung auf der Clanplanet LIGA beginnst du <a href="#cpl_site=register">hier</a>.
        </small>
    </div>
    
    <br /><br />
    
    2. Ich habe keinen Clanplanet Account, kann ich mich trotzdessen Registrieren?<br />
    <div style="padding-left:20px;">
        <small>
            Nein. Nutzer ohne Clanplanet Account können nicht teilnehmen. Hier kannst du dir einen <a href="http://www.clanplanet.de/anmelden.asp">Clanplanet Account erstellen</a>.
        </small>
    </div>
    
    <br /><br />
    
    3. Kann ich meinen Nutzernamen auf der Clanplanet LIGA nicht selber einstellen?<br />
    <div style="padding-left:20px;">
        <small>
            Nein. Dein Benutzername auf der Clanplanet LIGA entspricht immer dem Benutzernamen welchen du auch auf dem Clanplanet nutzt.
            Du kannst allerdings deinen Benutzernamen auf dem Clanplanet ändern. Sobald du deinen Benutzernamen geändert hast, dauert es maximal eine Woche
            bis dein Benutzername auch auf der Clanplanet LIGA übernommen wird. Mehr informationen findest du in den Einstellungen deines Clanplanet LIGA Accounts.
        </small>
    </div>
    
    <br /><br />
    
    4. Warum muss ich mich auf dem Clanplanet einloggen um mit der Anmeldung zu beginnen?<br />
    <div style="padding-left:20px;">
        <small>
            Die Clanplanet LIGA erkennt deinen Clanplanet Loginstatus und verarbeitet deinen Clanplanet Benutzernamen so, dass du keine weiteren Angaben machen musst.
        </small>
    </div>
    
    <br /><br />
    
    5. Werden schon vor dem Klick auf "Jetzt Registrieren" Daten von mir gespeichert?<br />
    <div style="padding-left:20px;">
        <small>
            Nein. Erst nach deinem Klick auf "Jetzt Registrieren" werden Daten von dir gespeichert. Ab diesem Punkt speichern wir deine:
            
            <br /><br />
            - Clanplanet Benutzernummer
            <br /><br />
            
            Klickst du anschließend auf den Link in der Clanplanet PM, werden weitere Daten von dir erstellt und gespeichert:
            
            <br /><br />
            - Clanplanet Benuternummer<br />
            - Clanplanet Benutzername<br />
            - Das von dir angebende Passwort während der Regisrtierung.<br />
            - Der Registrierungszeitpunkt.<br />
            <br /><br />
            
            Solltest du mehr Informationen zu denen von dir gespeicherten Daten haben wollen, kannst du in deinen Accounteinstellungen der Clanplanet LIGA eine Datenspiegelung beantragen.
            Dieser Vorgang kann allerdings unter bestimmten Umständen mehrere Stunden dauern, daher Informieren wird dich mit einer PM sobald deine Datenspiegelung abgeschlossen wurde.
        </small>
    </div>
    
</div>

