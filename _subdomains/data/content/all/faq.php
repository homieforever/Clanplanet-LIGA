<strong>Fragen zum Login</strong>
<br /><br />
<div style="padding:8px;">
    
    1. Warum steht mein Benutzername vor der Eingabe bereits im Feld?<br />
    <div style="padding-left:20px;">
        <small>
            Wir möchten es dir im Optimalfall so einfach wie möglich gestalten. Von daher lesen wir sofern du bereits
            auf dem Clanplanet eingeloggt bist deinen Benutzernamen aus und tragen ihn anschließend bereits in das Feld für deinen Benutzernamen ein.
        </small>
    </div>
    
    <br /><br />
    
    2. Was bringt es mir das "Merken" Feld anzuklicken?<br />
    <div style="padding-left:20px;">
        <small>
            Das sogenannte "merken" beim Login ist eine Zusatzfunktion, klickt man diesen Punkt an, so wird man immer automatisch eingeloggt. Die Funktion deaktivierst du in dem du das Clanplanet LIGA Logout nutzt, willst du aber das die Funktion
            aktiviert bleibt, so logge dich in Zukunft nicht mehr aus.
        </small>
    </div>
    
</div>

<br /><br />

<strong>Fragen zur "Passwort vergessen?" Funktion</strong>
<br /><br />
<div style="padding:8px;">
    
    1. Warum muss ich ein neues Passwort wählen und bekomme nicht einfach mein altes Passwort mitgeteilt?<br />
    <div style="padding-left:20px;">
        <small>
            Wir als Team der Clanplanet LIGA schreiben SICHERHEIT groß. Von daher wird dein Passwort verschlüsselt gespeichert und lässt sich nicht entschlüsseln.
            Wir speichern dein Passwort nur verschlüsselt um, die höchstmögliche Sicherheit bei einem Datenbankangriff zu Gewährleisten. Selbst wenn unsere Datenbank also nachgibt und
            es angreifern einmal leicht machen sollte haben die Angreifer keine Chance dein Passwort zu entschüsseln.
        </small>
    </div>
    
    <br /><br />
    
    2. Warum muss ich keinerlei angaben bei dieser Funktion machen?<br />
    <div style="padding-left:20px;">
        <small>
            Auch hier setzt die Clanplanet LIGA ihr Konzept weiter fort, wir möchten dir den aufenthalt auf unserer Plattform so einfach wie möglich gestalten.
            Weshalb auch hier das Clanplanet Login als authentifikation dient.
        </small>
    </div>
    
</div>

